import numpy as np
from numpy.testing import assert_allclose

import magpylib_jax as mpj
from magpylib_jax.constants import MU0


def _check_HBMJ_consistency(source_type: str, **kwargs):
    b = np.asarray(mpj.getB(source_type, **kwargs))
    h = np.asarray(mpj.getH(source_type, **kwargs))
    m = np.asarray(mpj.getM(source_type, **kwargs))
    j = np.asarray(mpj.getJ(source_type, **kwargs))
    assert_allclose(m * MU0, j)
    assert_allclose(b, MU0 * h + j)
    return h, b, m, j


